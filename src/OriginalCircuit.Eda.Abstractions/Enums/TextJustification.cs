namespace OriginalCircuit.Eda.Enums;

/// <summary>
/// Text justification (alignment) for labels and text objects.
/// Combines horizontal and vertical alignment in a single enum.
/// </summary>
public enum TextJustification
{
    /// <summary>Bottom-left alignment.</summary>
    BottomLeft = 0,

    /// <summary>Bottom-center alignment.</summary>
    BottomCenter = 1,

    /// <summary>Bottom-right alignment.</summary>
    BottomRight = 2,

    /// <summary>Middle-left alignment.</summary>
    MiddleLeft = 3,

    /// <summary>Middle-center alignment.</summary>
    MiddleCenter = 4,

    /// <summary>Middle-right alignment.</summary>
    MiddleRight = 5,

    /// <summary>Top-left alignment.</summary>
    TopLeft = 6,

    /// <summary>Top-center alignment.</summary>
    TopCenter = 7,

    /// <summary>Top-right alignment.</summary>
    TopRight = 8
}
